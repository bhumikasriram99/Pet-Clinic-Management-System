    private void btnshowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnshowActionPerformed
        // TODO add your handling code here:
        try
        {
            Class.forName("oracle.jdbc.driver.OracleDriver");
        }
        catch(ClassNotFoundException ce)
        {
            System.out.println(ce);
        }
        try
        {

            String inv=txtno.getText();

            String invdate="";
            String customer="";
            String company="";
            String product="";
            int quantity=0;
            int rate=0;
            int amount=0;
            int gst=0;
            int tax=0;
            int net=0;

            Connection con=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe","bhumika","bhumika");
            Statement st=con.createStatement();

            ResultSet rs = st.executeQuery("select * from prodsale where invoicenumber='"+inv+"'");

            while(rs.next())
            {

                invdate=rs.getString(2);
                customer=rs.getString(3);
                company=rs.getString(4);
                product=rs.getString(5);
                quantity=rs.getInt(6);
                rate=rs.getInt(6);
                amount=rs.getInt(7);
                gst=rs.getInt(8);
                tax=rs.getInt(9);
                net=rs.getInt(10);
            }
            txtno.setText(inv);
            txtinvdate.setText(invdate);
            txtsupplier.setText(customer);
            txtcustomer.setText(company);
            txtproduct.setText(product);

            txtquantity.setText(""+quantity);
            txtrate.setText(""+rate);
            txtamount.setText(""+amount);
            txtgst.setText(""+gst);
            txttax.setText(""+tax);
            txtnet.setText(""+net);
        }

        catch(SQLException se)
        {
            System.out.println(se);
        }
    }//GEN-LAST:event_btnshowActionPerformed

