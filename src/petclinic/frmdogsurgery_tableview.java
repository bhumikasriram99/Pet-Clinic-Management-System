    private void btnshowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnshowActionPerformed
        // TODO add your handling code here:
        String[]columnnames={"Surgery Id","Surgery Name","Surgery Type","Amonut"};
        DefaultTableModel model=new DefaultTableModel();
        model.setColumnIdentifiers(columnnames);
        jTable1.setModel(model);
        jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_ALL_COLUMNS);

        try
        {
            Class.forName("oracle.jdbc.driver.OracleDriver");
        }
        catch(ClassNotFoundException ce)
        {
            System.out.println(ce);
        }
        try
        {

            String sid=cmbsid.getSelectedItem().toString();
            String sname="";
            String stype="";
            String amount="";
            

            Connection con=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe","bhumika","bhumika");
            Statement st=con.createStatement();

            ResultSet rs;
            rs = st.executeQuery("select * from dogsurgery where surgeryid='"+sid+"'" );

            while(rs.next())
            {
                sid=rs.getString(1);
                sname=rs.getString(2);
                stype=rs.getString(3);
                amount=rs.getString(4);
                

                model.addRow(new Object[]{sid,sname,stype,amount});
            }

        }
        catch(SQLException se)
        {
            System.out.println(se);
        }
    }//GEN-LAST:event_btnshowActionPerformed

