    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:

        try
        {
            Class.forName("oracle.jdbc.driver.OracleDriver");
        }
        catch(ClassNotFoundException ce)
        {
            System.out.println(ce);
        }
        try
        {
            String pid =txtpid.getText();
            String pname=txtpname.getText();
            String cname=txtcname.getText();
            String mdate=txtmdate.getText();
            String edate=txtedate.getText();
            String dosage=txtdosage.getText();
            JOptionPane sc;
            sc = new JOptionPane();
            if(pname.equals(""))
            {
                sc.showMessageDialog(sc,"Please enter Product name");

            }
            else if(cname.equals(""))
            {
                sc.showMessageDialog(sc,"Please enter Customer Name");
            }
            else if(mdate.equals(""))
            {
                sc.showMessageDialog(sc,"Please enter Manufacture Date");
            }
            else if(edate.equals(""))
            {
                sc.showMessageDialog(sc,"Please enter Expiry Date");
            }
            else if(dosage.equals(""))
            {
                sc.showMessageDialog(sc,"Please enter Dosage");
            }

            else
            {
                Connection con=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe","bhumika","bhumika");
                Statement st=con.createStatement();

                String sql = "insert into medicine values('"+pid+"','"+pname+"','"+cname+"','"+mdate+"','"+edate+"','"+dosage+"')";
                st.executeUpdate(sql);

                Statement st1=con.createStatement();
                String sql1="insert into medstock values('"+pname+"','"+cname+"',0)";
                st1.executeUpdate(sql1);

                sc.showMessageDialog(sc,"Data saved successfully");
                txtpid.setText("");
                txtpname.setText("");
                txtcname.setText("");
                txtmdate.setText("");

                txtedate.setText("");
                txtdosage.setText("");
            }

        }
        catch(SQLException se)
        {
            System.out.println(se);
        }
    }//GEN-LAST:event_btnsaveActionPerformed

