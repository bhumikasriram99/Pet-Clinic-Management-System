    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        try
        {
            Class.forName("oracle.jdbc.driver.OracleDriver");
        }
        catch(ClassNotFoundException ce)
        {
            System.out.println(ce);
        }
        try
        {
            String pid =cmbpid.getSelectedItem().toString();
            String pname=txtpname.getText();
            String cname=txtcname.getText();
            String mdate=txtmdate.getText();
            String edate=txtedate.getText();
            String unit=txtunit.getText();
            String pcat=txtpcat.getText();
            
            JOptionPane sc;
            sc = new JOptionPane();
            if(pname.equals(""))
            {
                sc.showMessageDialog(sc,"Please enter Product Name");

            }
            else if(cname.equals(""))
            {
                sc.showMessageDialog(sc,"Please enter Customer Name");
            }
            else if(mdate.equals(""))
            {
                sc.showMessageDialog(sc,"Please enter Manufacture Date");
            }
            else if(edate.equals(""))
            {
                sc.showMessageDialog(sc,"Please enter Expiry Date");
            }
            else if(unit.equals(""))
            {
                sc.showMessageDialog(sc,"Please enter Dosage");
            }
            
            else
            {
                Connection con=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe","bhumika","bhumika");
                Statement st=con.createStatement();

                String sql = "update product set productname='"+pname+"',companyname='"+cname+"',productcategory='"+pcat+"',manufacturedate='"+mdate+"',expirydate='"+edate+"',units='"+unit+"' where  productid='"+pid+"'";
                st.executeUpdate(sql);

                sc.showMessageDialog(sc,"Data updated successfully");

            }

        }
        catch(SQLException se)
        {
            System.out.println(se);
        }
    }//GEN-LAST:event_btnupdateActionPerformed

