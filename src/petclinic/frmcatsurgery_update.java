    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:

        try
        {
            Class.forName("oracle.jdbc.driver.OracleDriver");
        }
        catch(ClassNotFoundException ce)
        {
            System.out.println(ce);
        }
        try
        {

            String id=cmbid.getSelectedItem().toString();

            String type="";
            String name="";
            int amt=0;

            Connection con=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe","bhumika","bhumika");
            Statement st=con.createStatement();

            ResultSet rs = st.executeQuery("select * from catsurgery where surgeryid='"+id+"'");

            while(rs.next())
            {

                type=rs.getString(2);
                name=rs.getString(3);
                amt=rs.getInt(4);

            }
            //cmbpid.setSelectedItem(pid);
            cmbtype.setSelectedItem(type);
            cmbname.setSelectedItem(name);
            txtamt.setText(""+amt);

        }

        catch(SQLException se)
        {
            System.out.println(se);
        }
    }//GEN-LAST:event_btnsearchActionPerformed

