    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        try
        {

            Class.forName("oracle.jdbc.driver.OracleDriver");
        }
        catch(ClassNotFoundException ce)
        {
            System.out.println(ce);
        }
        try
        {    JOptionPane sc=new JOptionPane();
            String id=cmbid.getSelectedItem().toString();
            if(id.equals(""))
            {
                sc.showMessageDialog(sc,"Please Enter surgery id");
            }

            else
            {

                int result;
                result=sc.showConfirmDialog(sc,"Confirm Delete?");
                if(result==sc.YES_OPTION)
                {
                    Connection con=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe","bhumika","bhumika");
                    Statement st=con.createStatement();
                    String sql="delete from catsurgery where surgeryid='"+id+"'";
                    st.executeQuery(sql);
                    sc.showMessageDialog(sc,"Deleted Successfully");
                    cmbid.setSelectedItem("");
                    txtamt.setText("");

                }
                else
                {
                    sc.showMessageDialog(sc," Data Not Deleted");
                    cmbid.setSelectedItem("");
                    txtamt.setText("");
                }
            }
        }
        catch(SQLException e)
        {
            System.out.println(e);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btndeleteActionPerformed

